<div class="container">
    <h2 id="como-funciona">Como Funciona o Aprendizado por Reforço?</h2>

    <div class="componente">
        <p><strong>Exemplo 1: Robô Aprendendo a Andar</strong></p>
        <p>Imagine um robô que está aprendendo a andar em linha reta. No começo, ele tropeça e cai. Mas cada vez que ele dá mais passos sem cair, recebe uma <strong>recompensa positiva</strong>. Se cair, recebe uma <strong>recompensa negativa</strong>. Com isso, o robô aprende, por tentativa e erro, os movimentos mais eficientes para andar com equilíbrio.</p>
    </div>

    <div class="componente">
        <p><strong>Exemplo 2: Jogando Pac-Man com IA</strong></p>
        <p>Um agente começa a jogar Pac-Man aleatoriamente. Ao longo das partidas, ele percebe que comer fantasmas e frutas dá <strong>mais pontos</strong>, enquanto ser pego por um fantasma encerra o jogo. Com base nessas experiências, ele adapta sua estratégia para obter a maior pontuação possível.</p>
    </div>

    <div class="componente">
        <p><strong>Exemplo 3: Assistente Virtual Ajustando Volume</strong></p>
        <p>Um assistente de voz ajusta o volume da música conforme a reação do usuário. Se o usuário disser "muito alto", ele reduz o volume e aprende que a ação anterior não foi ideal. Se o usuário não reclamar, ele entende que o volume está adequado. Assim, o sistema aprende a se ajustar sozinho.</p>
    </div>

    <div class="componente">
        <p><strong>Exemplo 4: Drone de Entrega</strong></p>
        <p>Um drone que entrega pizzas aprende qual é a melhor rota para ser rápido e evitar obstáculos. Se ele realiza uma entrega eficiente, recebe uma <strong>recompensa alta</strong>. Se demora ou colide com algo, a <strong>recompensa é menor</strong>. Com o tempo, o drone escolhe os melhores caminhos automaticamente.</p>
    </div>
</div>
